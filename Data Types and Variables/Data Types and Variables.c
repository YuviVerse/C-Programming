#include <stdio.h>

int main() {
    int integerVar = 10;
    float floatVar = 3.14;
    double doubleVar = 3.14159265359;
    char charVar = 'A';

    printf("Integer Variable: %d\n", integerVar);
    printf("Float Variable: %f\n", floatVar);
    printf("Double Variable: %lf\n", doubleVar);
    printf("Character Variable: %c\n", charVar);

    return 0;
}